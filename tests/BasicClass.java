public class BasicClass {
	public static int a = 1;
	public int b = 2;

	public void setB(int c) {
		b = c;
	}

	public int getB() {
		return b;
	}

	public static void setA(int c) {
		a = c;
	}

	public static void getA() {
		return a;
	}
}
